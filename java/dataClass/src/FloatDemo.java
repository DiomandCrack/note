public class FloatDemo {
   public static void main(String[] args){
      //定义一个单精度浮点型变量，存放1234.328
      float f = 1234.328f;
      System.out.println("f="+f);
      //定义一个双精度浮点型变量，存放5623.465
      double d = 5623.464;
      System.out.println("d="+d);
      double d1 = 123;
      System.out.println("d1="+d1);
   }
}
